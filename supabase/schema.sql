create or replace function public.validate_booking()
returns trigger
language plpgsql
security definer
set search_path = public
as $$
declare
  target_room public.rooms%rowtype;
  local_start timestamp;
  local_end timestamp;
  requested_hours numeric;
  daily_booked_hours numeric;
  day_start timestamptz;
  day_end timestamptz;
begin
  if new.status in ('rejected', 'cancelled', 'completed') then
    return new;
  end if;

  select * into target_room from public.rooms where id = new.room_id for share;
  if not found then raise exception using errcode = 'P0001', message = '존재하지 않는 특별실입니다.'; end if;

  local_start := new.start_time at time zone 'Asia/Seoul';
  local_end := new.end_time at time zone 'Asia/Seoul';

  if new.start_time >= new.end_time then
    raise exception using errcode = 'P0001', message = '종료 시간은 시작 시간보다 늦어야 합니다.';
  end if;
  if new.start_time <= now() then
    raise exception using errcode = 'P0001', message = '이미 지난 시간에는 예약할 수 없습니다.';
  end if;
  if local_start::date <> local_end::date then
    raise exception using errcode = 'P0001', message = '예약은 같은 날짜 안에서만 가능합니다.';
  end if;
  if local_start::time < target_room.available_start or local_end::time > target_room.available_end then
    raise exception using errcode = 'P0001', message = '특별실 이용 가능 시간을 벗어났습니다.';
  end if;
  requested_hours := extract(epoch from (new.end_time - new.start_time)) / 3600.0;
  day_start := local_start::date::timestamp at time zone 'Asia/Seoul';
  day_end := (local_start::date + 1)::timestamp at time zone 'Asia/Seoul';

  perform pg_advisory_xact_lock(
    hashtext(new.room_id::text),
    (local_start::date - date '2000-01-01')::integer
  );

  if tg_op = 'UPDATE' then
    select coalesce(sum(extract(epoch from (b.end_time - b.start_time)) / 3600.0), 0)
      into daily_booked_hours
    from public.bookings b
    where b.room_id = new.room_id
      and b.id <> old.id
      and public.booking_blocks_daily_limit(b.status)
      and b.start_time >= day_start
      and b.start_time < day_end;
  else
    select coalesce(sum(extract(epoch from (b.end_time - b.start_time)) / 3600.0), 0)
      into daily_booked_hours
    from public.bookings b
    where b.room_id = new.room_id
      and public.booking_blocks_daily_limit(b.status)
      and b.start_time >= day_start
      and b.start_time < day_end;
  end if;

  if daily_booked_hours + requested_hours > target_room.max_booking_hours then
    raise exception using errcode = 'P0001', message = '최대 예약 시간을 초과했습니다.';
  end if;
  if new.people > target_room.capacity then
    raise exception using errcode = 'P0001', message = '특별실 최대 수용 인원을 초과했습니다.';
  end if;
  if target_room.status <> 'available' or target_room.maintenance_mode then
    raise exception using errcode = 'P0001', message = '현재 점검 또는 이용 중지 상태인 특별실입니다.';
  end if;
  if target_room.closed_until is not null and target_room.closed_until > new.start_time then
    raise exception using errcode = 'P0001', message = '해당 시간에는 특별실이 일시 폐쇄 상태입니다.';
  end if;
  return new;
end;
$$;

create or replace function public.guard_booking_write()
returns trigger
language plpgsql
security definer
set search_path = public
as $$
declare
  actor uuid := auth.uid();
  actor_role public.user_role := public.current_user_role();
  only_decision_changed boolean;
begin
  if auth.role() = 'service_role' then return new; end if;

  if tg_op = 'INSERT' then
    if actor is null or (new.user_id <> actor and actor_role <> 'admin') then
      raise exception using errcode = '42501', message = '다른 사용자의 예약을 생성할 수 없습니다.';
    end if;
    if actor_role in ('teacher', 'admin') then
      new.status := 'approved';
      new.approved_by := actor;
      new.approved_at := now();
    else
      new.status := 'pending';
      new.approved_by := null;
      new.approved_at := null;
    end if;
    return new;
  end if;

  if actor_role = 'admin' then
    if new.status = 'cancelled' and old.status <> 'cancelled' then new.cancelled_at := now(); end if;
    return new;
  end if;

  only_decision_changed :=
    new.room_id is not distinct from old.room_id and
    new.user_id is not distinct from old.user_id and
    new.purpose is not distinct from old.purpose and
    new.people is not distinct from old.people and
    new.start_time is not distinct from old.start_time and
    new.end_time is not distinct from old.end_time and
    new.created_at is not distinct from old.created_at and
    new.cancelled_at is not distinct from old.cancelled_at;

  if actor_role = 'teacher' and old.user_id <> actor then
    if old.status <> 'pending' or new.status not in ('approved', 'rejected') or not only_decision_changed then
      raise exception using errcode = '42501', message = '교사는 대기 중인 예약의 승인 또는 거절만 처리할 수 있습니다.';
    end if;
    new.approved_by := actor;
    new.approved_at := now();
    return new;
  end if;

  if old.user_id = actor then
    if old.status not in ('pending', 'approved') then
      raise exception using errcode = '42501', message = '처리 완료된 예약은 수정할 수 없습니다.';
    end if;
    if new.status = 'cancelled' then
      if new.room_id is distinct from old.room_id or
         new.user_id is distinct from old.user_id or
         new.purpose is distinct from old.purpose or
         new.people is distinct from old.people or
         new.start_time is distinct from old.start_time or
         new.end_time is distinct from old.end_time or
         new.created_at is distinct from old.created_at or
         new.approved_by is distinct from old.approved_by or
         new.approved_at is distinct from old.approved_at then
        raise exception using errcode = '42501', message = '예약 취소와 정보 변경을 동시에 처리할 수 없습니다.';
      end if;
      new.approved_by := old.approved_by;
      new.approved_at := old.approved_at;
      new.cancelled_at := now();
      return new;
    end if;
    if actor_role = 'student' and old.status = 'approved' then
      raise exception using errcode = '42501', message = '승인 완료된 예약은 수정할 수 없습니다. 변경이 필요하면 취소 후 다시 신청해 주세요.';
    end if;
    if new.status is distinct from old.status or
       new.user_id is distinct from old.user_id or
       new.approved_by is distinct from old.approved_by or
       new.approved_at is distinct from old.approved_at or
       new.cancelled_at is distinct from old.cancelled_at or
       new.created_at is distinct from old.created_at then
      raise exception using errcode = '42501', message = '예약 상태 또는 소유자는 직접 변경할 수 없습니다.';
    end if;
    return new;
  end if;

  raise exception using errcode = '42501', message = '예약을 변경할 권한이 없습니다.';
end;
$$;

