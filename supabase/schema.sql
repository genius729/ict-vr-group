create extension if not exists btree_gist;


do $$ begin
  create type public.user_role as enum ('student', 'teacher', 'admin');
exception when duplicate_object then null; end $$;

do $$ begin
  create type public.booking_status as enum ('pending', 'approved', 'rejected', 'cancelled', 'completed');
exception when duplicate_object then null; end $$;

create table if not exists public.users (
  id uuid primary key references auth.users(id) on delete cascade,
  name text not null check (char_length(name) between 1 and 80),
  email text not null unique,
  role public.user_role not null default 'student',
  grade smallint check (grade between 1 and 3),
  class_number smallint check (class_number between 1 and 30),
  student_number smallint check (student_number between 1 and 50),
  created_at timestamptz not null default now()
);

create table if not exists public.rooms (
  id bigint generated by default as identity primary key,
  name text not null unique check (char_length(name) between 1 and 100),
  location text not null check (char_length(location) between 1 and 120),
  capacity integer not null check (capacity > 0 and capacity <= 500),
  status text not null default 'available' check (status in ('available', 'unavailable')),
  description text not null default '',
  image_url text,
  available_start time not null default '08:00',
  available_end time not null default '18:00',
  max_booking_hours numeric(4,2) not null default 3 check (max_booking_hours > 0 and max_booking_hours <= 24),
  maintenance_mode boolean not null default false,
  closed_until timestamptz,
  created_at timestamptz not null default now(),
  constraint rooms_available_time_check check (available_start < available_end)
);

create table if not exists public.bookings (
  id bigint generated by default as identity primary key,
  room_id bigint not null references public.rooms(id) on delete restrict,
  user_id uuid not null references public.users(id) on delete restrict,
  approved_by uuid references public.users(id) on delete set null,
  purpose text not null check (char_length(purpose) between 2 and 500),
  people integer not null check (people > 0),
  start_time timestamptz not null,
  end_time timestamptz not null,
  status public.booking_status not null default 'pending',
  created_at timestamptz not null default now(),
  approved_at timestamptz,
  cancelled_at timestamptz,
  constraint bookings_time_order check (start_time < end_time)
);

-- pending 요청도 시간대를 선점합니다. 동시 INSERT 경쟁은 DB가 원자적으로 차단합니다.
do $$
begin
  if not exists (
    select 1
    from pg_constraint
    where conname = 'bookings_no_time_overlap'
      and conrelid = 'public.bookings'::regclass
  ) then
    alter table public.bookings
    add constraint bookings_no_time_overlap
    exclude using gist (
      room_id with =,
      tstzrange(start_time, end_time, '[)') with &&
    ) where (status in ('pending', 'approved'));
  end if;
end $$;

create index if not exists bookings_user_id_idx on public.bookings(user_id);
create index if not exists bookings_room_start_idx on public.bookings(room_id, start_time);
create index if not exists bookings_status_idx on public.bookings(status);

create table if not exists public.notifications (
  id bigint generated by default as identity primary key,
  user_id uuid not null references public.users(id) on delete cascade,
  title text not null,
  content text not null,
  is_read boolean not null default false,
  created_at timestamptz not null default now()
);
create index if not exists notifications_user_created_idx on public.notifications(user_id, created_at desc);

create table if not exists public.booking_logs (
  id bigint generated by default as identity primary key,
  booking_id bigint not null references public.bookings(id) on delete cascade,
  action text not null,
  actor_id uuid references public.users(id) on delete set null,
  previous_data jsonb,
  new_data jsonb,
  created_at timestamptz not null default now()
);
create index if not exists booking_logs_booking_idx on public.booking_logs(booking_id, created_at desc);

