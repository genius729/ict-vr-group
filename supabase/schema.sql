create or replace function public.log_booking_change()
returns trigger
language plpgsql
security definer
set search_path = public
as $$
declare action_name text;
begin
  if tg_op = 'INSERT' then action_name := 'created';
  elsif new.status = 'approved' and old.status <> 'approved' then action_name := 'approved';
  elsif new.status = 'rejected' and old.status <> 'rejected' then action_name := 'rejected';
  elsif new.status = 'cancelled' and old.status <> 'cancelled' then
    action_name := case when public.is_admin() and auth.uid() <> old.user_id then 'force_cancelled' else 'cancelled' end;
  else action_name := 'updated';
  end if;

  insert into public.booking_logs (booking_id, action, actor_id, previous_data, new_data)
  values (
    new.id,
    action_name,
    auth.uid(),
    case when tg_op = 'UPDATE' then to_jsonb(old) else null end,
    to_jsonb(new)
  );
  return new;
end;
$$;

drop trigger if exists bookings_audit on public.bookings;
create trigger bookings_audit
  after insert or update on public.bookings
  for each row execute function public.log_booking_change();

create or replace function public.notify_booking_change()
returns trigger
language plpgsql
security definer
set search_path = public
as $$
declare room_name text;
declare actor_role public.user_role := public.current_user_role();
begin
  if old.status is not distinct from new.status then return new; end if;
  select name into room_name from public.rooms where id = new.room_id;

  if new.status = 'approved' then
    insert into public.notifications(user_id, title, content)
    values (new.user_id, '예약이 승인되었습니다', room_name || ' 예약이 승인되었습니다.');
  elsif new.status = 'rejected' then
    insert into public.notifications(user_id, title, content)
    values (new.user_id, '예약이 거절되었습니다', room_name || ' 예약이 거절되었습니다.');
  elsif new.status = 'cancelled' then
    insert into public.notifications(user_id, title, content)
    values (
      new.user_id,
      case when actor_role = 'admin' and auth.uid() <> new.user_id then '예약이 강제 취소되었습니다' else '예약이 취소되었습니다' end,
      room_name || ' 예약이 취소되었습니다.'
    );
  end if;
  return new;
end;
$$;

drop trigger if exists bookings_notify on public.bookings;
create trigger bookings_notify
  after update of status on public.bookings
  for each row execute function public.notify_booking_change();

-- pg_cron이 매분 호출하며 시작 10분 전 알림을 중복 없이 생성합니다.
create or replace function public.create_booking_reminders()
returns void
language sql
security definer
set search_path = public
as $$
  insert into public.notifications(user_id, title, content)
  select b.user_id, '예약 시작 10분 전입니다',
         r.name || ' 예약이 ' || to_char(b.start_time at time zone 'Asia/Seoul', 'HH24:MI') || '에 시작됩니다.'
  from public.bookings b
  join public.rooms r on r.id = b.room_id
  where b.status = 'approved'
    and b.start_time > now() + interval '9 minutes'
    and b.start_time <= now() + interval '10 minutes'
    and not exists (
      select 1 from public.notifications n
      where n.user_id = b.user_id
        and n.title = '예약 시작 10분 전입니다'
        and n.content = r.name || ' 예약이 ' || to_char(b.start_time at time zone 'Asia/Seoul', 'HH24:MI') || '에 시작됩니다.'
        and n.created_at > now() - interval '30 minutes'
    );
$$;



-- 교사/관리자 전용 실제 DB 통계
create or replace function public.get_room_daily_booking_usage(
  target_room_id bigint,
  target_date date,
  exclude_booking_id bigint default null
)
returns jsonb
language plpgsql
security definer
set search_path = public
as $$
declare
  target_room public.rooms%rowtype;
  day_start timestamptz := target_date::timestamp at time zone 'Asia/Seoul';
  day_end timestamptz := (target_date + 1)::timestamp at time zone 'Asia/Seoul';
  booked_hours numeric;
begin
  if auth.uid() is null then
    raise exception using errcode = '42501', message = '로그인이 필요합니다.';
  end if;

  select * into target_room from public.rooms where id = target_room_id;
  if not found then
    raise exception using errcode = 'P0001', message = '존재하지 않는 특별실입니다.';
  end if;

  select coalesce(sum(extract(epoch from (b.end_time - b.start_time)) / 3600.0), 0)
    into booked_hours
  from public.bookings b
  where b.room_id = target_room_id
    and public.booking_blocks_daily_limit(b.status)
    and (exclude_booking_id is null or b.id <> exclude_booking_id)
    and b.start_time >= day_start
    and b.start_time < day_end;

  return jsonb_build_object(
    'room_id', target_room.id,
    'date', target_date,
    'booked_hours', booked_hours,
    'max_booking_hours', target_room.max_booking_hours,
    'remaining_hours', greatest(target_room.max_booking_hours - booked_hours, 0)
  );
end;
$$;

create or replace function public.get_booking_statistics(target_month date default date_trunc('month', now())::date)
returns jsonb
language plpgsql
security definer
set search_path = public
as $$
declare result jsonb;
begin
  if not public.is_staff() then
    raise exception using errcode = '42501', message = '통계를 조회할 권한이 없습니다.';
  end if;

  with bounds as (
    select target_month::timestamptz as from_at,
           (target_month + interval '1 month')::timestamptz as to_at
  ), monthly as (
    select count(*)::int total,
           count(*) filter (where status = 'approved')::int approved,
           count(*) filter (where status = 'cancelled')::int cancelled
    from public.bookings, bounds
    where start_time >= from_at and start_time < to_at
  ), room_usage as (
    select r.name,
           count(b.id)::int bookings,
           coalesce(round(sum(extract(epoch from (b.end_time - b.start_time)) / 3600)::numeric, 1), 0) hours,
           coalesce(round(least(
             100,
             (sum(extract(epoch from (b.end_time - b.start_time)) / 3600) /
               nullif(
                 (extract(epoch from (r.available_end - r.available_start)) / 3600) *
                 ((target_month + interval '1 month')::date - target_month),
                 0
               )) * 100
           )::numeric, 1), 0) usage_rate
    from public.rooms r
    left join public.bookings b on b.room_id = r.id and b.status in ('approved','completed')
      and b.start_time >= (select from_at from bounds) and b.start_time < (select to_at from bounds)
    group by r.id, r.name, r.available_start, r.available_end order by hours desc
  ), grades as (
    select coalesce(u.grade::text, '미입력') grade, count(*)::int count
    from public.bookings b join public.users u on u.id = b.user_id, bounds
    where b.start_time >= from_at and b.start_time < to_at
    group by u.grade
  ), hourly_stats as (
    select
      to_char(start_time at time zone 'Asia/Seoul', 'HH24') as hour_of_day,
      count(*)::int as booking_count
    from public.bookings, bounds
    where start_time >= from_at and start_time < to_at
    group by hour_of_day
    order by hour_of_day
  ), trend as (
    select
      to_char(months.month_value, 'YYYY-MM') as month,
      count(b.id)::int as count
    from generate_series(
           date_trunc('month', target_month) - interval '5 months',
           date_trunc('month', target_month),
           interval '1 month'
         ) as months(month_value)
    left join public.bookings b
      on b.start_time >= months.month_value
     and b.start_time < months.month_value + interval '1 month'
    group by months.month_value
    order by months.month_value
  )
  select jsonb_build_object(
    'summary', (select jsonb_build_object(
      'total', total,
      'approved', approved,
      'cancelled', cancelled,
      'approval_rate', case when total = 0 then 0 else round(approved * 100.0 / total, 1) end,
      'cancellation_rate', case when total = 0 then 0 else round(cancelled * 100.0 / total, 1) end
    ) from monthly),
    'rooms', coalesce((select jsonb_agg(to_jsonb(ru)) from room_usage as ru), '[]'::jsonb),
    'grades', coalesce((select jsonb_agg(to_jsonb(g)) from grades as g), '[]'::jsonb),
    'hours', coalesce((select jsonb_agg(to_jsonb(h)) from hourly_stats as h), '[]'::jsonb),
    'trend', coalesce((select jsonb_agg(to_jsonb(t)) from trend as t), '[]'::jsonb)
  ) into result;
  return result;
end;
$$;

create or replace function public.delete_room_if_no_active_bookings(target_room_id bigint)
returns void
language plpgsql
security definer
set search_path = public
as $$
declare
  blocking_count integer;
begin
  if not public.is_admin() then
    raise exception using errcode = '42501', message = '관리자만 특별실을 삭제할 수 있습니다.';
  end if;

  select count(*) into blocking_count
  from public.bookings
  where room_id = target_room_id
    and status = 'approved'
    and end_time > now();

  if blocking_count > 0 then
    raise exception using errcode = 'P0001', message = '승인된 예정 또는 진행 예약이 있는 특별실은 삭제할 수 없습니다. 먼저 예약을 취소해 주세요.';
  end if;

  delete from public.bookings
  where room_id = target_room_id;

  delete from public.rooms
  where id = target_room_id;

  if not found then
    raise exception using errcode = 'P0001', message = '존재하지 않는 특별실입니다.';
  end if;
end;
$$;

create or replace function public.delete_booking_as_admin(target_booking_id bigint)
returns void
language plpgsql
security definer
set search_path = public
as $$
begin
  if not public.is_admin() then
    raise exception using errcode = '42501', message = '관리자만 예약 신청 내역을 삭제할 수 있습니다.';
  end if;

  delete from public.bookings
  where id = target_booking_id;

  if not found then
    raise exception using errcode = 'P0001', message = '존재하지 않는 예약입니다.';
  end if;
end;
$$;

